`timescale 1ns / 1ps

module tb_ALU_topmodule;

reg clk;

reg [7:0] data_in;
reg [4:0] opcode;

reg load_a;
reg load_b;

wire [15:0] led;

wire [3:0] digit;
wire [7:0] Seven_Seg;

ALU_topmodule DUT(

    .clk(clk),

    .data_in(data_in),
    .opcode(opcode),

    .load_a(load_a),
    .load_b(load_b),

    .led(led),

    .digit(digit),
    .Seven_Seg(Seven_Seg)

);


//--------------------------------------------------
// Clock Generation
//--------------------------------------------------

initial begin
    clk = 0;
    forever #5 clk = ~clk;   // 100 MHz equivalent
end


//--------------------------------------------------
// Test Sequence
//--------------------------------------------------

initial begin

    $display("Starting Top Module Simulation");

    data_in = 0;
    opcode  = 0;

    load_a  = 0;
    load_b  = 0;

    #20;

    //--------------------------------------------------
    // Load A = 25
    //--------------------------------------------------

    data_in = 8'd25;

    load_a = 1;

    #10;

    load_a = 0;

    #20;

    //--------------------------------------------------
    // Load B = 10
    //--------------------------------------------------

    data_in = 8'd10;

    load_b = 1;

    #10;

    load_b = 0;

    #20;

    //--------------------------------------------------
    // ADD
    //--------------------------------------------------

    opcode = 5'b00000;

    #20;

    //--------------------------------------------------
    // SUB
    //--------------------------------------------------

    opcode = 5'b00001;

    #20;

    //--------------------------------------------------
    // MUL
    //--------------------------------------------------

    opcode = 5'b00010;

    #20;

    //--------------------------------------------------
    // DIV
    //--------------------------------------------------

    opcode = 5'b00011;

    #20;

    //--------------------------------------------------
    // AND
    //--------------------------------------------------

    opcode = 5'b00101;

    #20;

    //--------------------------------------------------
    // OR
    //--------------------------------------------------

    opcode = 5'b00110;

    #20;

    //--------------------------------------------------
    // XOR
    //--------------------------------------------------

    opcode = 5'b00111;

    #20;

    //--------------------------------------------------
    // Load New A = -5
    //--------------------------------------------------

    data_in = 8'hFB;     // -5

    load_a = 1;

    #10;

    load_a = 0;

    #20;

    //--------------------------------------------------
    // ADD
    //--------------------------------------------------

    opcode = 5'b00000;

    #20;

    //--------------------------------------------------
    // Load New B = 2
    //--------------------------------------------------

    data_in = 8'd2;

    load_b = 1;

    #10;

    load_b = 0;

    #20;

    //--------------------------------------------------
    // Arithmetic Right Shift
    //--------------------------------------------------

    opcode = 5'b10001;

    #20;

    //--------------------------------------------------
    // Overflow Test
    //--------------------------------------------------

    data_in = 8'd127;

    load_a = 1;

    #10;

    load_a = 0;

    #20;

    data_in = 8'd1;

    load_b = 1;

    #10;

    load_b = 0;

    #20;

    opcode = 5'b00000;

    #20;

    $display("Simulation Complete");

    $finish;

end

endmodule
